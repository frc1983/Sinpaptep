<h1>Sindicato</h1>
<h3>Um pouco sobre a fundação do sindicato</h3>

<p class="first justify">No dia 1º de maio de 1975, às 20h, em Porto Alegre, em um recinto denominado 
Plenarinho, no 3º andar do “Palácio Farroupilha”, edifício da Assembleia 
Legislativa do Estado do Rio Grande do Sul, na Praça Deodoro, reuniram-se os 
exercentes da categoria profissional dos trabalhadores em empresas de 
publicidade de Porto Alegre para resolverem a respeito da fundação e 
legalização da respectiva Associação Profissional dos Publicitários. 
Assim, com as iniciais transcritas da 1ª Ata Pró Fundação da Associação 
dos Publicitários que, graças aos esforços de vários profissionais liderados 
pelo Sr. Omar Siqueira, na ocasião eleito primeiro presidente da Entidade, 
que em ata datada de 18 de abril de 1975 a Entidade tornou-se oficial, 
permanecendo como presidente da mesma o Sr. Omar Siqueira. Em 02 de outubro 
de 1975 a Associação recebeu seu registro, de número 578, junto à Delegacia 
Regional do Trabalho.</p>

<p class="first justify">Após anos como Associação, em ata de número 9, datada de 27 de julho de 1978, 
foi realizada uma Assembleia para transformar a Associação, já sob a presidência 
do Sr. Jim Pereira Pompeu, em sindicato, que finalmente após aprovada por 
maioria e após feitas as documentações necessárias foi enviada ao Ministério 
do Trabalho, em Brasília. Finalmente, em 13 de dezembro de 1979 recebeu a 
carta sob nome de SINDICATO DOS TRABALHADORES EM EMPRESAS DE PUBLICIDADE 
DE PORTO ALEGRE, sendo o Sr. Jim Pereira Pompeu, que permaneceu no cargo 
até julho/89, quando por eleição realizada foi eleito o novo presidente, 
Sr. Décio José Azevedo Rojas, que permaneceu na função até 04 de 
junho de 1990. Após esta data assumiu a diretoria o 
Sr. Jurandir de Oliveira Maciel, que permaneceu no cargo até a 
nova eleição, no dia 21 de julho, quando foi reempossado, no cargo 
até seu falecimento em 27/04/15.</p>

<p class="first justify">Percebendo a atual diretoria da necessidade de o Sindicato se expandir para 
atender às necessidades dos Publicitários do interior e não havendo outro 
Sindicato para atender a Categoria fora de Porto Alegre foi marcada, no dia 
23 de novembro de 1995, uma Assembleia para aprovação extensão de base, que 
foi aprovada por unanimidade e, assim, foi posta à aprovação de novo Estatuto 
e nome para o Sindicato, que, em 23 de novembro de 1995 passou a chamar-se 
SINDICATO DOS PUBLICITÁRIOS, AGENCIADORES DE PROPAGANDA E TRABALHADORES EM 
EMPRESAS DE PUBLICIDADE DO ESTADO DO RIO GRANDE DO SUL, com sede na 
Av. João Wallig, 1658 - sala 405.</p>

<h3>Histórico de nossos processos de ampliação de base</h3>

<p class="first justify">No dia 24 de outubro de 1995 reuniu-se a Diretoria do Sindicato dos 
Trabalhadores em Empresas de Publicidade de Porto Alegre, com o fim de 
discutir a possibilidade de extensão de base para todo o estado e a criação 
de novo Estatuto Social. As propostas foram apresentadas, sendo aprovadas 
pela Diretoria o modelo de Estatuto que seria colocado para apreciação e 
votação da categoria, sendo que para isto, seria marcada Assembleia Geral. 
No dia 20 de novembro de 1995, então, o Sindicato, através de Edital publicado 
no Diário Oficial e no Jornal do Comércio, convocou a Categoria a comparecer 
na Assembleia Geral a ser realizada no dia 23 de novembro de 1995, na sede 
desta entidade para o fim de apreciar e aprovar ou não a nossa expansão.</p>

<p class="first justify">Iniciada a Assembleia as propostas de extensão de base e de alteração 
Estatutária foram apresentadas para apreciação da categoria, sendo aprovadas 
por unanimidade pelos presentes a Assembleia, assim também como a alteração 
no nome do Sindicato, que passaria a se denominar Sindicato dos Publicitários,
Agenciadores de Propaganda e Trabalhadores em Empresas de Publicidade do 
Estado do Rio grande do Sul, tudo registrado na Ata da Assembleia. Após isto,
vencidos todos os trâmites legais, e para o cumprimento do que determina o 
artigo 8º da Constituição Federal/88, foi feito o registro do novo Estatuto 
Social desta Entidade, passando a denominar-se definitivamente como Sindicato 
dos Publicitários, Agenciadores de Propaganda e Trabalhadores em Empresas de 
Publicidade do Estado do Rio Grande do Sul, conforme registro no Cartório de 
Registro Especiais de Porto Alegre, ocorrido dia 15 de março de 1996, 
fls. 192 vº, sob o número 13, tendo adquirido, à partir daqui, personalidade 
jurídica, que garantiu a abrangência estadual de toda a categoria.</p>

<p class="first justify">Em meados do mês de julho de 1996, recebemos o reconhecimento do Tribunal 
Regional do Trabalho da 4º Região, que homologou o primeiro Dissídio Coletivo 
desta entidade, já sob a nova denominação e com âmbito estadual, conforme 
processo RVDC nº 96009052-5, Dissídio Coletivo este obtido através de um 
acordo firmado com o sindicato Patronal (que também reconheceu nossa 
ampliação), com todos os direitos nele contidos, retroativos ao mês de maio 
do mesmo ano. Posteriormente, o último degrau em direção ao nosso 
reconhecimento total como representante dos trabalhadores de nossa 
categoria em todo o estado, foi atingido em 26 de junho de 1998, quando 
obtivemos do Ministério do Trabalho, em Brasília, o Registro de Alteração 
Estatuária na Denominação de Extensão de Base Territorial, após a publicação, 
no Diário Oficial deste dia, do despacho do Ministro da Relação de Trabalho, 
página 08 da seção I, em cumprimento da Instrução Normativa nº 01 de 17.07.97, 
o qual havia sido solicitado em 27 de fevereiro de 1996, conforme 
protocolo nº 46000.001842/96-87, determinado, enfim, o término de nosso 
processo de ampliação de base.</p>

<h6>(texto extraído dos jornais informativo nº 1 de setembro de 1996 e nº 2 de 
    agosto de 1999)</h6>
<p class="first justify">Atualmente o Sindicato dos Publicitários, Agenciadores de Propaganda e 
Trabalhadores em Empresas de Publicidade do Estado do Rio Grande do Sul, 
está situado na Av. João Wallig, nº518, com sua sede própria.</p>
