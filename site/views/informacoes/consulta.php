<h3>Consolidação das leis do trabalho (CLT)</h3>
<p class="first justify">A Consolidação das Leis do Trabalho (CLT) é a principal norma legislativa brasileira referente ao Direito do trabalho e o Direito processual do trabalho. Ela foi criada através do Decreto-Lei nº 5.452, de 1 de maio de 1943 e sancionada pelo então presidente Getúlio Vargas durante o período do Estado Novo , unificando toda legislação trabalhista então existente no Brasil.
    Seu objetivo principal é a regulamentação das relações individuais e coletivas do trabalho, nela previstas.</p>
